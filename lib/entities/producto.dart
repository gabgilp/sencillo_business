class Producto {
  final int id;
  final String nombre;
  final String descripcion;
  final double precio;
  final int idEmpresa;
  final int idRubro;

  Producto({
    required this.id,
    required this.nombre,
    required this.descripcion,
    required this.precio,
    required this.idEmpresa,
    required this.idRubro,
  });
}
